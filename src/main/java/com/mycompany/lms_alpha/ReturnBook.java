    public void sIdSearch(String keyword) {

        String sql = "select * from issue where StudentCode ='" + keyword + "'";
        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {

                jTextField3.setText(rs.getString(10));
                jTextField4.setText(rs.getString(11));
                jTextField5.setText(rs.getString(12));
                jTextField6.setText(rs.getString(13));

                rs.close();
                pst.close();

            } else {
                JOptionPane.showMessageDialog(null, "Student Not Found");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void searchBooksSid(String keyword) {
        String sql = "select * from issue where StudentCode ='" + keyword + "'";
        try {
            pst = conn.prepareStatement(sql);

//          
            rs = pst.executeQuery();
            jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
            tableModel.setRowCount(0);
            while (rs.next()) {
                Object[] row = new Object[7];
                row[0] = rs.getString("IssueID");
                row[1] = rs.getString("BookID");
                row[2] = rs.getString("Name");
                row[3] = rs.getString("Author");
                row[4] = rs.getString("Edition");
                row[5] = rs.getString("Publisher");
                row[6] = rs.getString("Time");

                System.out.println(rs.getString("BookID"));

                tableModel.addRow(row);

            }
            if (tableModel.getRowCount() == 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "No books found.", "Info", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Database Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
    }

