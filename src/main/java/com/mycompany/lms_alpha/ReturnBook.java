    private void issueHistoryCreate() {
        Integer selectedIssueId = null;
        int selectedRow = jTable1.getSelectedRow(); // Get the selected row index
        
        LocalDateTime now = LocalDateTime.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        String formattedDateTime = now.format(formatter);

        if (selectedRow >= 0) {
            // Retrieve data from the selected row using the column indexes
            selectedIssueId = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());   // Column 0 -> Book ID

            // Now you can use these values for further processing
            System.out.println("Selected Issue ID: " + selectedIssueId);

        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "No row selected!", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }

//        Object input = selectedBookId;
        String sql = """
    INSERT INTO issueHistory (IssueID,BookID, Name, Author, Edition, Publisher, Price, Quantity, StudentCode, SName, Department, Semester, PhoneNo, Time)
    SELECT IssueID, BookID, Name, Author, Edition, Publisher, Price, Quantity, StudentCode, SName, Department, Semester, PhoneNo, Time
    FROM issue WHERE IssueId = ?
""";

        String deleteSql = "DELETE FROM issue WHERE IssueId = ?";

        String returnTimeSql = "UPDATE issueHistory SET ReturnTime = ? WHERE IssueID = ?";

        try {
            pst = conn.prepareStatement(sql);
            pst.setInt(1, selectedIssueId); // Set the IssueId parameter
            int rowsInserted = pst.executeUpdate();
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Data transferred successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "No matching records found for IssueId: " + selectedIssueId);
            }
            try (PreparedStatement updatePst = conn.prepareStatement(returnTimeSql)) {
            updatePst.setString(1, formattedDateTime);
            updatePst.setInt(2, selectedIssueId);
            int rowsUpdated = updatePst.executeUpdate();
            if (rowsUpdated == 0) {
                throw new SQLException("Failed to update ReturnTime. Aborting transaction.");
            }
        }
            try (PreparedStatement deletePst = conn.prepareStatement(deleteSql)) {
                deletePst.setInt(1, selectedIssueId);
                int rowsDeleted = deletePst.executeUpdate();
                if (rowsDeleted == 0) {
                    throw new SQLException("No rows deleted. Aborting transaction.");
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(IssueBookNext.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error transferring data: " + ex.getMessage());
        }
    }

