    private void getAdminName() {

        String sql = "select Name from account";
        
        try{
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
                //Capitalizing First letters
                String data = rs.getString(1);
                String capitalisedData = capitalizeFirstLetters(data);
                jLabel5.setText(capitalisedData);
            }
            rs.close();
            pst.close();
           
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            
        }

    }
    
    private String capitalizeFirstLetters(String input) {
    if (input == null || input.isEmpty()) {
        return input; // Return the input if it's null or empty
    }

    String[] words = input.split("\\s+"); // Split the string into words
    StringBuilder capitalized = new StringBuilder();

    for (String word : words) {
        if (word.length() > 0) {
            // Capitalize the first letter and append the rest of the word
            capitalized.append(Character.toUpperCase(word.charAt(0)))
                       .append(word.substring(1).toLowerCase())
                       .append(" "); // Add a space after each word
        }
    }

    return capitalized.toString().trim(); // Remove trailing space
}

    

    private void updateDateTime() {
        // Get current date and time
        LocalDateTime now = LocalDateTime.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyy HH:mm:ss");
        formattedDateTime = now.format(formatter);

        // Set the formatted date and time to the label
        jLabel3.setText("Date: " + formattedDateTime);

    }

