    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow(); // Get the selected row index

        if (selectedRow >= 0) {
            // Retrieve data from the selected row using the column indexes
            Object selectedBookId = jTable1.getValueAt(selectedRow, 0);   // Column 0 -> Book ID
            // Column 6 -> Quantity

            // Now you can use these values for further processing
            System.out.println("Selected Book ID: " + selectedBookId);

            String deleteSql = "DELETE FROM books WHERE BookID = ?";
            try (PreparedStatement deletePst = conn.prepareStatement(deleteSql)) {
                deletePst.setInt(1, Integer.parseInt(selectedBookId.toString()));
                int rowsDeleted = deletePst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data deleted successfully!");
                if (rowsDeleted == 0) {
                    throw new SQLException("No rows deleted. Aborting transaction.");
                }
            } catch (SQLException ex) {
                Logger.getLogger(IssueBookNext.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Error transferring data: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

